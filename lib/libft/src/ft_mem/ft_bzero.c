#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{	
	size_t	i;
	char	*s_tmp;

	i = 0;
	s_tmp = s;
	while (i < n)
	{
		s_tmp[i] = '\0';
		i++;
	}
	s = s_tmp;
	return (NULL);
}
