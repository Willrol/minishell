#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{	
	size_t	i;
	char	*s_tmp;

	i = 0;
	s_tmp = s;
	while (i < n)
	{
		s_tmp[i] = c;
		i++;
	}
	s = s_tmp;
	return (s);
}
